/**
 * operadores de comparacion
 */

// es igual = (compara solo el valor)

let s = 50
let p =  "50"

let resulta = s == p

console.log(resulta)

// estrictamente igual === (compara valor y tipo de dato)

let v = 50
let x =  "50"

let resultado = v === x

console.log(resultado)

// es diferente != (verifica que los valores sian diferentes)

let f = 24
let h = 6

let resul = f != h

console.log(resul)

// es estrictamente  diferente != (verifica que los valores sian diferentes)

let g = 24
let l = "6"

let result = g !== l

console.log(result)

// se pueden hacer en texto  tambien y en numeros

let y = "alberto"
let w = "luis"

let resu = y != w

console.log(resu)









